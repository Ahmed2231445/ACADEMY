function applyTheme(theme) {
  document.body.classList.toggle('theme-brown', theme === 'brown');
  try {
    localStorage.setItem('studyhouse-theme', theme);
  } catch (e) {}

  const textEl = document.getElementById('themeToggleText');
  if (textEl) {
    const lang = document.documentElement.getAttribute('lang') === 'ar' ? 'ar' : 'en';
    // نعكس النص: إذا كان الثيم بني، نعرض "ذهبي" والعكس
    const displayKey = theme === 'brown' ? 'theme-gold' : 'theme-brown';
    textEl.textContent = translations[displayKey]?.[lang] || displayKey;
  }
}

function applyLanguage(lang) {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const entry = translations[key];
    if (entry && entry[lang]) el.textContent = entry[lang];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    const entry = translations[key];
    if (entry && entry[lang]) el.setAttribute("placeholder", entry[lang]);
  });

  document.documentElement.setAttribute("lang", lang);
  document.body.classList.toggle("lang-ar", lang === "ar");

  const label = document.querySelector(".lang-toggle-label");
  if (label) label.textContent = lang === "ar" ? "English" : "عربي";

  try {
    localStorage.setItem("studyhouse-lang", lang);
  } catch (e) {}
}

const CONTENT_DOC_EXPORT_URL =
  "https://script.google.com/macros/s/AKfycbwkhBvUYDA0dv2HFNRsdu03FEveHqC_HoMy0-CWyUAsLO7EuFlypydPLCb2Pl0qMOcn/exec";

function parseCourseContentText(text) {
  const lines = text.split(/\r?\n/);
  const courseKeyMap = { coding: "programming", arabic: "arabic", other: "other", english: "english" };
  const data = {
    programming: { video: [], files: [] },
    arabic: { video: [], files: [] },
    other: { video: [], files: [] },
    english: { video: [], files: [] },
  };

  let currentCourse = null;
  let currentSection = null;

  lines.forEach((raw) => {
    const line = raw.trim();
    if (!line) return;

    const courseMatch = line.match(/^(coding|arabic|other|english)\s*:?\s*$/i);
    if (courseMatch) {
      currentCourse = courseKeyMap[courseMatch[1].toLowerCase()];
      currentSection = null;
      return;
    }

    const sectionMatch = line.match(/^(video|files)\s*:?\s*$/i);
    if (sectionMatch && currentCourse) {
      currentSection = sectionMatch[1].toLowerCase() === "video" ? "video" : "files";
      return;
    }

    const itemMatch = line.match(/^(\d+)\s*:\s*(.*)$/);
    if (itemMatch && currentCourse && currentSection) {
      data[currentCourse][currentSection].push({
        number: parseInt(itemMatch[1], 10),
        link: itemMatch[2].trim(),
      });
    }
  });

  return data;
}

async function fetchCourseContent() {
  const res = await fetch(CONTENT_DOC_EXPORT_URL, { cache: "no-store" });
  if (!res.ok) throw new Error("تعذر تحميل محتوى الكورسات");
  const text = await res.text();
  return parseCourseContentText(text);
}

const LOGIN_DOC_EXPORT_URL =
  "https://script.google.com/macros/s/AKfycbzVNINaQo46CQdhiwJ6g-CjDjLYBLvlW7WKXukW9oRmStZg-VptTzGjkstjvMlXJWWecw/exec";

async function fetchLoginCredentials() {
  const res = await fetch(LOGIN_DOC_EXPORT_URL, { cache: "no-store" });
  if (!res.ok) throw new Error("تعذر تحميل بيانات الدخول");
  const text = await res.text();

  const lines = text.split(/\r?\n/);
  const dataIndex = lines.findIndex((line) =>
    line.trim().toLowerCase().startsWith("data")
  );
  if (dataIndex === -1) return {};

  const creds = {};
  for (let i = dataIndex + 1; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;
    if (line.includes("/////")) break;

    const parts = line.split(":").map((p) => p.trim());
    if (parts.length < 2) continue;

    const user = parts[0];
    const pass = parts[1];
    if (!user || !pass) continue;

    const courses = parts.slice(2).map((c) => c.toLowerCase()).filter(Boolean);

    creds[user] = { password: pass, courses };
  }
  return creds;
}

const COMMUNITY_DOC_EXPORT_URL =
  "https://script.google.com/macros/s/AKfycbzD0Tj6KYsT3XqNM7Sic0P0390uk3wpkUlKtpINFVFX1YkJqctRld95xu7Enklhnc63/exec";

function parseCommunityText(text) {
  const lines = text.split(/\r?\n/);
  const courseKeyMap = { coding: "programming", arabic: "arabic", other: "other", english: "english" };
  const data = { programming: [], arabic: [], other: [], english: [] };

  let currentCourse = null;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;

    // بمجرد الوصول لقسم "الاختبارات" نوقف القراءة نهائيًا
    // عشان مانخلطش بين رسائل المجتمع ومحتوى الاختبارات
    if (/^الاختبارات\s*:?\s*$/.test(line)) break;

    if (/^المجتمع\s*:?\s*$/.test(line)) continue;

    const courseMatch = line.match(/^(coding|arabic|other|english)\s*:?\s*$/i);
    if (courseMatch) {
      currentCourse = courseKeyMap[courseMatch[1].toLowerCase()];
      continue;
    }

    if (!currentCourse) continue;

    const sep = line.indexOf(":");
    if (sep === -1) continue;

    const username = line.slice(0, sep).trim();
    const message = line.slice(sep + 1).trim();
    if (!username || !message) continue;

    data[currentCourse].push({ username, message });
  }

  return data;
}

async function fetchCommunityMessages() {
  const res = await fetch(COMMUNITY_DOC_EXPORT_URL, { cache: "no-store" });
  if (!res.ok) throw new Error("تعذر تحميل رسائل المجتمع");
  const text = await res.text();
  return parseCommunityText(text);
}

async function sendCommunityMessage(courseKey, username, message) {
  const body = new URLSearchParams({ action: "send", course: courseKey, username, message });
  const res = await fetch(COMMUNITY_DOC_EXPORT_URL, { method: "POST", body });
  if (!res.ok) throw new Error("تعذر إرسال الرسالة");
}

async function deleteCommunityMessage(courseKey, index, username) {
  const body = new URLSearchParams();

  body.append("action", "delete");
  body.append("course", courseKey);
  body.append("index", String(index));
  body.append("username", username);

  const res = await fetch(COMMUNITY_DOC_EXPORT_URL, {
    method: "POST",
    body: body
  });

  const result = (await res.text()).trim();

  console.log("Delete response:", result);

  if (result !== "ok") {
    throw new Error(result);
  }
}


