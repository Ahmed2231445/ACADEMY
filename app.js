(function() {
  const textarea = document.getElementById('pronunciationText');
  const playBtn = document.getElementById('pronunciationPlayBtn');
  const stopBtn = document.getElementById('pronunciationStopBtn');
  const repeatBtn = document.getElementById('pronunciationRepeatBtn');
  const statusEl = document.getElementById('pronunciationStatus');
  const voiceInfo = document.getElementById('voiceInfo');

  if (!textarea || !playBtn || !stopBtn || !repeatBtn) return;

  let utterance = null;
  let isSpeaking = false;
  let isPaused = false;
  let selectedVoice = null;

  // تحميل الصوت الإنجليزي الرجالي (British)
  function loadEnglishVoice() {
    return new Promise((resolve) => {
      const voices = window.speechSynthesis.getVoices();
      // البحث عن صوت بريطاني رجالي
      let voice = voices.find(v => v.lang.startsWith('en-GB') && v.name.toLowerCase().includes('male'));
      if (!voice) voice = voices.find(v => v.lang.startsWith('en-GB'));
      if (!voice) voice = voices.find(v => v.lang.startsWith('en'));
      if (!voice && voices.length) voice = voices[0];
      selectedVoice = voice || null;
      if (voiceInfo) {
        voiceInfo.textContent = voice ? `Voice: ${voice.name} (${voice.lang})` : 'Voice: Default';
      }
      resolve(voice);
    });
  }

  // تهيئة الأصوات
  if (window.speechSynthesis) {
    window.speechSynthesis.getVoices();
    window.speechSynthesis.onvoiceschanged = () => {
      loadEnglishVoice();
    };
    setTimeout(loadEnglishVoice, 300);
  }

  // دالة إنشاء كائن النطق
  function createUtterance(text) {
    const u = new SpeechSynthesisUtterance(text);
    u.lang = 'en-GB';
    u.rate = 0.9;
    u.pitch = 1.0;
    u.volume = 1.0;
    if (selectedVoice) u.voice = selectedVoice;
    u.onstart = () => {
      isSpeaking = true;
      isPaused = false;
      statusEl.textContent = '🔊';
    };
    u.onend = () => {
      isSpeaking = false;
      isPaused = false;
      statusEl.textContent = '⏸️';
    };
    u.onerror = () => {
      isSpeaking = false;
      isPaused = false;
      statusEl.textContent = '❌';
    };
    u.onpause = () => {
      isPaused = true;
      statusEl.textContent = '⏸️';
    };
    u.onresume = () => {
      isPaused = false;
      statusEl.textContent = '🔊';
    };
    return u;
  }

  function speakText(text) {
    if (!text.trim()) {
      statusEl.textContent = '⚠️';
      setTimeout(() => statusEl.textContent = '⏸️', 1000);
      return;
    }
    if (window.speechSynthesis) {
      window.speechSynthesis.cancel();
    }
    utterance = createUtterance(text);
    window.speechSynthesis.speak(utterance);
  }

  function stopSpeech() {
    if (window.speechSynthesis) {
      window.speechSynthesis.cancel();
    }
    isSpeaking = false;
    isPaused = false;
    statusEl.textContent = '⏸️';
  }

  function repeatSpeech() {
    const text = textarea.value.trim();
    if (!text) return;
    stopSpeech();
    setTimeout(() => speakText(text), 100);
  }

  playBtn.addEventListener('click', () => {
    const text = textarea.value.trim();
    if (!text) return;
    if (isSpeaking) {
      stopSpeech();
      setTimeout(() => speakText(text), 150);
    } else {
      speakText(text);
    }
  });

  stopBtn.addEventListener('click', stopSpeech);
  repeatBtn.addEventListener('click', repeatSpeech);

  textarea.addEventListener('input', () => {
    if (isSpeaking) stopSpeech();
  });

  function updatePronunciationLang() {
    const isAr = document.documentElement.getAttribute('lang') === 'ar';
    textarea.placeholder = isAr ? 'اكتب أو الصق النص الإنجليزي هنا...' : 'Type or paste English text here...';
  }

  // ربط بتحديث اللغة
  const origApplyLang = window.applyLanguage;
  if (origApplyLang) {
    window.applyLanguage = function(lang) {
      origApplyLang(lang);
      updatePronunciationLang();
    };
  }
  setTimeout(updatePronunciationLang, 100);

  // عند ظهور القسم، ننشط الأصوات
  const observer = new MutationObserver(() => {
    const content = document.getElementById('appPronunciationContent');
    if (content && content.style.display !== 'none') {
      loadEnglishVoice();
    }
  });
  observer.observe(document.getElementById('appPronunciationContent') || document.body, { attributes: true, attributeFilter: ['style'] });
})();

