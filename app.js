  function parseExamLines(lines) {
    const courseKeyMap = { coding: "programming", arabic: "arabic", other: "other", english: "english" };
    const data = { programming: [], arabic: [], other: [], english: [] };
    let currentCourse = null;

    lines.forEach((raw) => {
      const line = raw.trim();
      if (!line) return;
      if (/^الاختبارات\s*:?\s*$/.test(line)) return;

      const courseMatch = line.match(/^(coding|arabic|other|english)\s*:?\s*$/i);
      if (courseMatch) { currentCourse = courseKeyMap[courseMatch[1].toLowerCase()]; return; }

      if (currentCourse && line.indexOf("EXAMSDATA:") === 0) {
        const jsonStr = line.substring("EXAMSDATA:".length).trim();
        try {
          const arr = JSON.parse(jsonStr);
          if (Array.isArray(arr)) data[currentCourse] = arr;
        } catch (e) {}
      }
    });
    return data;
  }

  async function fetchExamData() {
    const raw = await fetchAcademyRaw();
    const lines = raw.split(/\r?\n/);
    let examIdx = -1;
    lines.forEach((l, i) => { if (examIdx === -1 && /^الاختبارات\s*:?\s*$/.test(l.trim())) examIdx = i; });
    const examLines = examIdx !== -1 ? lines.slice(examIdx) : [];
    return parseExamLines(examLines);
  }

