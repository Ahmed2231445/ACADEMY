  async function handleDeleteClick(courseKey, index, btnEl) {
    const confirmed = confirm(isArabicNow() ? "هل تريد حذف هذه الرسالة؟" : "Delete this message?");
    if (!confirmed) return;

    const username = getSavedUser();
    if (!username) return;

    if (btnEl) btnEl.disabled = true;
    try {
      await deleteCommunityMessage(courseKey, index, username);
      await refreshCommunity(true);
    } catch (err) {
      alert(isArabicNow() ? "تعذر حذف الرسالة، حاول مرة أخرى" : "Couldn't delete the message, try again");
      if (btnEl) btnEl.disabled = false;
    }
  }

